public class OOPSBannerApp {

    public static void main(String[] args) {

        String[] lines = {
            "  ***     ***    *****   ***** ",
            " *   *   *   *   *    *  *     ",
            " *   *   *   *   *****   ***** ",
            " *   *   *   *   *           *  ",
            "  ***     ***    *       ***** "
        };

        for (String line : lines) {   // small 'l'
            System.out.println(line);
        }
    }
}